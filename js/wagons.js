function chooseFilter(param) {
    let parameter = parseInt(param.value);

    switch (parameter) {
        case 1:
            selectPassengerClass();
            break;
        case 2:
            selectFreightClass();
            break;
        case 3:
            selectPackagedProduct();
            break;
        case 4:
            selectDamagedPackaging();
            break;
        case 5:
            selectUnpackedProduct();
            break;
        case 6:
            selectPromoPrice();
            break;
        // case 7:
        //     ;
        //     break;
        // case 8:
        //     ;
        //     break;
        case 9:
            selectPriceRange1();
            break;
        case 10:
            selectPriceRange2();
            break;
        case 11:
            selectPriceRange3();
            break;
        case 12:
            selectPriceRange4();
            break;

        default:
            resetFilter();
            break;
    }
}

