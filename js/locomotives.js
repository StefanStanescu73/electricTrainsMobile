// Filters for mobile

function chooseFilter(param) {
    let parameter = parseInt(param.value);

    switch (parameter) {
        case 1:
            selectElectricClass();
            break;
        case 2:
            selectDieselClass();
            break;
        case 3:
            selectSteamClass();
            break;
        case 4:
            selectPackagedProduct();
            break;
        case 5:
            selectDamagedPackaging();
            break;
        case 6:
            selectUnpackedProduct();
            break;
        case 7:
            selectPromoPrice();
            break;
        // case 8:
        //     ;
        //     break;
        // case 9:
        //     ;
        //     break;
        case 10:
            selectPriceRange1();
            break;
        case 11:
            selectPriceRange2();
            break;
        case 12:
            selectPriceRange3();
            break;
        case 13:
            selectPriceRange4();
            break;

        default:
            resetFilter();
            break;
    }
}